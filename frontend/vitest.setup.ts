import '@testing-library/jest-dom/vitest'

// Ant Design relies on matchMedia for responsive behavior.
// jsdom doesn’t implement it by default.
Object.defineProperty(window, 'matchMedia', {
  writable: true,
  value: (query: string) => ({
    matches: false,
    media: query,
    onchange: null,
    addListener: () => {},
    removeListener: () => {},
    addEventListener: () => {},
    removeEventListener: () => {},
    dispatchEvent: () => false,
  }),
})

